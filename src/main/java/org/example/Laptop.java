package org.example;

public class Laptop {
    private String manufacturer;
    private String screenSize;
    private String resolution;

    private String screenType;
    private String screenTouchscreen;
    private String processorName;
    private Integer processorPhysicalCores;
    private String processorSpeed;
    private String ram;
    private String discStorage;
    private String discType;
    private String graphicCardName;
    private String graphicCardMemory;
    private String os;
    private String discReader;

    public String getManufacturer() {
        return manufacturer;
    }
    public void setManufacturer(String manufacturer) {
        this.manufacturer = manufacturer;
    }
    public String getScreenSize() {
        return screenSize;
    }
    public void setScreenSize(String screenSize) {
        this.screenSize = screenSize;
    }
    public String getScreenType() {
        return screenType;
    }
    public void setScreenType(String screenType) {
        this.screenType = screenType;
    }
    public String getScreenTouchscreen() {
        return screenTouchscreen;
    }
    public void setScreenTouchscreen(String screenTouchscreen) {
        this.screenTouchscreen = screenTouchscreen;
    }
    public String getProcessorName() {
        return processorName;
    }
    public void setProcessorName(String processorName) {
        this.processorName = processorName;
    }
    public Integer getProcessorPhysicalCores() {
        return processorPhysicalCores;
    }
    public void setProcessorPhysicalCores(Integer processorPhysicalCores) {
        this.processorPhysicalCores = processorPhysicalCores;
    }
    public String getRam() {
        return ram;
    }
    public void setRam(String ram) {
        this.ram = ram;
    }
    public String getDiscStorage() {
        return discStorage;
    }
    public void setDiscStorage(String discStorage) {
        this.discStorage = discStorage;
    }
    public String getDiscType() {
        return discType;
    }
    public void setDiscType(String discType) {
        this.discType = discType;
    }
    public String getGraphicCardName() {
        return graphicCardName;
    }
    public void setGraphicCardName(String graphicCardName) {
        this.graphicCardName = graphicCardName;
    }
    public String getGraphicCardMemory() {
        return graphicCardMemory;
    }
    public void setGraphicCardMemory(String graphicCardMemory) {
        this.graphicCardMemory = graphicCardMemory;
    }
    public String getOs() {
        return os;
    }
    public void setOs(String os) {
        this.os = os;
    }
    public String getDiscReader() {
        return discReader;
    }
    public void setDiscReader(String discReader) {
        this.discReader = discReader;
    }
    public String getProcessorSpeed() {
        return processorSpeed;
    }
    public void setProcessorSpeed(String processorSpeed) {
        this.processorSpeed = processorSpeed;
    }

    public String getResolution() {
        return resolution;
    }

    public void setResolution(String resolution) {
        this.resolution = resolution;
    }
}